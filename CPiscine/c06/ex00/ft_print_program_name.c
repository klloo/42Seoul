#include <unistd.h>

void	put_str(char *str)
{
	while (*str)
		write(1, str++, 1);
	write(1, "\n", 1);
}

int	main(int argc, char *argv[])
{
	(void) argc;
	put_str(argv[0]);
	return (0);
}
