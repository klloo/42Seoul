int	ft_str_is_numeric(char *str)
{
	char	c;

	if (*str == '\0')
		return (1);
	while (*str != '\0')
	{
		c = *str;
		if ('0' > c || c > '9')
			return (0);
		str++;
	}
	return (1);
}
